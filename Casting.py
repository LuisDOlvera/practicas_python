
# Conversiones o "Casting"

x = "123"
print("La variable x es: ", type(x))

#Convertimos el valor de la variable x de tipo "str" a "int"

x = int(x)
print("La variable x es: ", type(x))

# Conversión a int con la función int()
x = int(1)
print("x en int sería: ", (x))

y = int(5.5)
print("y en int sería: ", (y))

z = int("1")
print("z en int sería: ", (z))

#Conversión a float con la función float()
x = float(1)
print("x en float sería: ", (x))

y = float(6.6)
print("y en float sería: ", (y))

z = float("33")
print("z en float sería: ", (z))

w = float("41.2")
print("w en float sería: ", (w))

#Conversión a str con la función str()

x = str("a1")
print("x en string sería: ", (x))

y = str("2")
print("y en string sería: ", (y))

z = str(3.0)
print("z en string sería: ", (z))

w = str(True)
print("w en string sería: ", (w))

j = str(False)
print("j en string sería: ", (j))

